DROP TABLE IF EXISTS BOOKS;
DROP TABLE IF EXISTS AUTHORS;
DROP TABLE IF EXISTS GENRES;
CREATE TABLE BOOKS(ID BIGINT PRIMARY KEY, NAME VARCHAR(255), AUTHOR BIGINT, GENRE BIGINT);
CREATE TABLE AUTHORS(ID BIGINT PRIMARY KEY, AUTHOR_NAME VARCHAR(255), AUTHOR_SURNAME VARCHAR(255));
CREATE TABLE GENRES(ID BIGINT PRIMARY KEY, GENRE_NAME VARCHAR(255));